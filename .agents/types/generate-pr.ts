/**
 * 🔄 Generate PR — Contribuição Automática (Fase 5)
 *
 * Gera Pull Requests automaticamente para contribuir melhorias
 * de volta para repositórios originais (ECC, GitHub, etc.).
 *
 * Fluxo: fork → branch → commit → PR
 * Template de PR com descrição da melhoria + métricas.
 *
 * PLAN-F5: Contribuição Automática
 * SPEC-SEC-4.3: Gerenciador de Patches (contribuição de volta)
 */

import { Octokit } from '@octokit/rest'
import type { Patch } from './patch'

// ============================================================
// TIPOS
// ============================================================

/**
 * PrTemplate — Template de Pull Request
 */
export interface PrTemplate {
  /** Título do PR */
  title: string
  /** Corpo do PR em Markdown */
  body: string
  /** Branch de origem */
  head: string
  /** Branch de destino */
  base: string
  /** Labels do PR */
  labels: string[]
}

/**
 * PrMetadata — Metadados do PR para o template
 */
export interface PrMetadata {
  /** ID do patch que gerou o PR */
  patchId: string
  /** Artefato alvo */
  target: string
  /** Descrição da melhoria */
  description: string
  /** Métricas da melhoria */
  metrics: {
    linesChanged: number
    tokenReduction?: string
    filesModified: string[]
  }
  /** Autor do patch */
  author: string
  /** Data de criação */
  createdAt: string
  /** Repo upstream (ex: "affaan-m/ECC") */
  upstreamRepo: string
}

/**
 * PrResult — Resultado da geração do PR
 */
export interface PrResult {
  /** Se o PR foi gerado com sucesso (local ou remoto) */
  success: boolean
  /** Template gerado */
  template: PrTemplate
  /** Metadados usados */
  metadata: PrMetadata
  /** URL do PR (se enviado ao GitHub) */
  prUrl?: string
  /** Número do PR (se enviado ao GitHub) */
  prNumber?: number
  /** Mensagem de resultado */
  message: string
}

/**
 * PrStore — Interface de storage para PRs
 */
export interface PrStore {
  save(id: string, result: PrResult): void
  load(id: string): PrResult | null
  list(): string[]
}

// ============================================================
// STORE EM MEMÓRIA (para testes)
// ============================================================

/**
 * InMemoryPrStore — Store em memória para testes
 */
export class InMemoryPrStore implements PrStore {
  private store: Record<string, PrResult> = {}

  save(id: string, result: PrResult): void {
    this.store[id] = result
  }

  load(id: string): PrResult | null {
    return this.store[id] ?? null
  }

  list(): string[] {
    return Object.keys(this.store)
  }
}

